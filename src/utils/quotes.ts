// 隨機獲取一句語錄
export function getRandomQuote(): string {
  const randomIndex = Math.floor(Math.random() * PROFESSIONAL_QUOTES.length)
  return PROFESSIONAL_QUOTES[randomIndex]
}

// 獲取指定數量的語錄
export function getQuotes(count: number = 1): string[] {
  const shuffled = [...PROFESSIONAL_QUOTES].sort(() => 0.5 - Math.random())
  return shuffled.slice(0, Math.min(count, PROFESSIONAL_QUOTES.length))
}

// 根據關鍵字搜尋語錄
export function searchQuotes(keyword: string): string[] {
  const lowerKeyword = keyword.toLowerCase()
  return PROFESSIONAL_QUOTES.filter(quote => 
    quote.toLowerCase().includes(lowerKeyword)
  )
}

// 獲取語錄總數
export function getQuotesCount(): number {
  return PROFESSIONAL_QUOTES.length
}
